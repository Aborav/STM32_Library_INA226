/**
 * @brief get bus voltage in float
 * @return bus voltage (float)
 */
float INA_GetBusVoltage(void) {
		uint16_t value = INA_Receive(INA_VBUS_REG_ADDR);
		return value*0.00125F;	// LSB = 1.25mV = 0.00125V
}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// @brief read current from current register
/// @return float current
float INA_GetCurrent(void) {
#ifdef INA_USE_RECALIBRATION
		INA_SetCalibration(ina_calib_val); //in case of INA reboot
#endif
		int16_t value = INA_Receive(INA_CUR_REG_ADDR);
		float f_value = value * INA_CURRENT_LSB; // LSB рассчитывается на основе макс. ожидаемого тока, умножаем и возвращаем
		if (f_value<0) f_value*=-1;
		return f_value;                        
}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// @brief read INA power register
/// @return float power
float INA_GetPower(void) {
#ifdef INA_USE_RECALIBRATION
		INA_SetCalibration(ina_calib_val); //in case of INA reboot
#endif
		uint16_t value = INA_Receive(INA_POWER_REG_ADDR);  // Чтение регистра мощности
		return (uint16_t)(value * INA_POWER_LSB);                             // LSB в 25 раз больше LSB для тока, умножаем возвращаем
}
