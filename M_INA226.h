////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/**
 * @brief turn ON sleep mode
 * @param state--> 1-sleep,0-normal
 */
void INA_SleepMode(uint8_t state) {
		uint16_t cfg_register = INA_Receive(INA_CFG_REG_ADDR) & ~(0b111); //clear mode bits
		INA_Transmit(INA_CFG_REG_ADDR, cfg_register | (state ? 0b000 : 0b111)); //1-sleep,0-normal
}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/**
 * @brief send new calibration value to INA register
 * @param cal --> calibration value
 */
void INA_SetCalibration(uint16_t cal) {
		INA_Transmit(INA_CAL_REG_ADDR, cal); //modify calibration register
		ina_calib_val = cal; //modify global variable
}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/**
 * @brief read calibration register
 * @return calibration register
 */
uint16_t INA_GetCalibration(void) {
		ina_calib_val = INA_Receive(INA_CAL_REG_ADDR);
		return ina_calib_val;
}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/**
 * @brief increase or decrease calibration value
 * @param adj --> +32767 --- -32767
 */
void INA_ChangeCalibration(int16_t adj) {
		INA_SetCalibration(INA_GetCalibration() + adj);  //read and modify register
		ina_calib_val = ina_calib_val + adj; //modify global variable
}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/**
 * @brief set avarage grade in config register
 * @param avg --> 0 to 7
 */
void INA_SetAveraging(uint8_t avg) {
		uint16_t cfg_register = INA_Receive(INA_CFG_REG_ADDR) & ~(0b111 << 9);  // Читаем конф. регистр, сбросив биты AVG2-0
		INA_Transmit(INA_CFG_REG_ADDR, cfg_register | avg << 9);                // Пишем новое значение конф. регистр
}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/**
 * @brief set sampling time in config register
 * @param ch --> channel 3-shunt V, 6-bus V
 * @param samp_t --> sampling time 0 to 7
 */
void INA_SetSampleTime(uint8_t ch, uint8_t samp_t) {
		uint16_t cfg_register = INA_Receive(INA_CFG_REG_ADDR);  // Читаем конф. регистр
		cfg_register &= ~((0b111) << (ch ? 6 : 3));                 // Сбрасываем нужную пачку бит, в зависимости от канала
		cfg_register |= samp_t << (ch ? 6 : 3);                       // Пишем нужную пачку бит, в зависимости от канала
		INA_Transmit(INA_CFG_REG_ADDR, cfg_register);           // Пишем новое значение конф. регистра
}
